<script setup>
import { ref, computed, onMounted, onUnmounted } from 'vue';
import { Head, Link, router, usePage } from '@inertiajs/vue3';
import Dropdown from '@/Components/Dropdown.vue';
import DropdownLink from '@/Components/DropdownLink.vue';
import Banner from '@/Components/Banner.vue';
import Sidebar from '@/Components/Sidebar.vue';
import ThemeToggle from '@/Components/ThemeToggle.vue';
import OnboardingTour from '@/Components/OnboardingTour.vue';
import Spinner from '@/Components/Spinner.vue';
import { Menu, Search, Bell, HelpCircle } from 'lucide-vue-next';
import axios from 'axios'; // <-- NOVO: Importar o axios

// --- Props ---
defineProps({
    title: String,
});

// --- Lógica Principal ---
const page = usePage();
const isNavigating = ref(false);

router.on('start', () => {
    isNavigating.value = true;
});

router.on('finish', () => {
    isNavigating.value = false;
});

const isAdminTenant = computed(() => {
    return page.props.auth.user?.roles?.includes('Admin Tenant') || false;
});

const logout = () => {
    router.post(route('logout'));
};

const restartOnboardingTour = () => {
    window.dispatchEvent(new CustomEvent('restart-onboarding-tour'));
};

const themeStyles = computed(() => {
    const theme = page.props.theme;
    return {
        '--color-primary': theme?.primary || '#4F46E5',
        '--color-secondary': theme?.secondary || '#D946EF',
    };
});

const userPermissions = computed(() => page.props.auth.user?.permissions || []);
const authDebug = computed(() => page.props.auth?.debug || {});

// --- Lógica para Sidebar Responsiva ---
const isMobile = ref(window.innerWidth < 1024);
const isSidebarOpenOnMobile = ref(false);

function updateMobileStatus() {
    isMobile.value = window.innerWidth < 1024;
    if (!isMobile.value) {
        isSidebarOpenOnMobile.value = false;
    }
}

function toggleMobileSidebar() {
    isSidebarOpenOnMobile.value = !isSidebarOpenOnMobile.value;
}

// --- NOVO: Lógica de Notificações ---
const notifications = ref([]);
const showNotifications = ref(false);

const fetchNotifications = async () => {
    try {
        const response = await axios.get(route('notifications.index'));
        notifications.value = response.data;
    } catch (error) {
        console.error('Erro ao buscar notificações:', error);
    }
};

const markAsRead = async (notification) => {
    try {
        await axios.patch(`/notifications/${notification.id}/read`);
        // Remove a notificação da lista após ser lida para atualizar a UI
        notifications.value = notifications.value.filter(n => n.id !== notification.id);
    } catch (error) {
        console.error('Erro ao marcar notificação como lida:', error);
    }
};
// --- FIM: Lógica de Notificações ---


onMounted(() => {
    window.addEventListener('resize', updateMobileStatus);

    // --- NOVO: Buscar notificações ao montar e a cada 1 minuto ---
    fetchNotifications();
    setInterval(fetchNotifications, 60000);
});

onUnmounted(() => {
    window.removeEventListener('resize', updateMobileStatus);
    // Idealmente, o intervalo deve ser limpo também, mas para o layout principal pode não ser necessário.
});
</script>

<template>
    <div :style="themeStyles">
        <Head :title="title" />
        <Banner />

        <div v-if="isNavigating" class="fixed inset-0 bg-gray-900/40 backdrop-blur-sm flex justify-center items-center z-[100]">
            <Spinner />
        </div>

        <div class="min-h-screen bg-slate-50 dark:bg-[#0A1E1C]">
            <div class="flex h-screen">

                <div class="hidden lg:block lg:w-64 lg:flex-shrink-0 sidebar">
                    <Sidebar />
                </div>

                <div v-if="isMobile">
                    <div
                        v-if="isSidebarOpenOnMobile"
                        @click="toggleMobileSidebar"
                        class="fixed inset-0 z-20 bg-black/60 backdrop-blur-sm"
                        aria-hidden="true"
                    ></div>
                    <div :class="['fixed inset-y-0 left-0 z-30 w-64 transform transition-transform duration-300 ease-in-out sidebar', isSidebarOpenOnMobile ? 'translate-x-0' : '-translate-x-full']">
                        <Sidebar @close="toggleMobileSidebar" />
                    </div>
                </div>

                <div class="flex flex-1 flex-col overflow-hidden">

                    <header class="main-header">
                        <div class="flex items-center gap-4">
                            <button @click="toggleMobileSidebar" class="header-icon-btn lg:hidden" aria-label="Abrir menu">
                                <Menu class="h-6 w-6"/>
                            </button>
                            <div v-if="$slots.header" class="hidden lg:block">
                                <slot name="header" />
                            </div>
                        </div>

                        <div class="flex items-center gap-2 sm:gap-4">
                            <div class="hidden md:block">
                                <div class="relative">
                                    <span class="absolute inset-y-0 left-0 flex items-center pl-3">
                                        <Search class="h-5 w-5 text-gray-400" />
                                    </span>
                                    <input type="text" placeholder="Buscar..." class="w-48 lg:w-64 rounded-md border-gray-300 bg-gray-100 py-2 pl-10 pr-4 text-sm focus:border-[var(--color-primary)] focus:ring-[var(--color-primary)] dark:border-gray-700/50 dark:bg-gray-800/50 dark:text-gray-300">
                                </div>
                            </div>

                            <div class="relative">
                                <button @click="showNotifications = !showNotifications" class="header-icon-btn relative">
                                    <Bell class="h-6 w-6" />
                                    <span v-if="notifications.length > 0" class="absolute -top-1 -right-1 inline-flex items-center justify-center h-5 w-5 text-xs font-bold text-white bg-red-500 rounded-full">
                                        {{ notifications.length }}
                                    </span>
                                </button>

                                <div v-if="showNotifications" @click.away="showNotifications = false" class="absolute right-0 mt-2 w-80 bg-white rounded-md shadow-lg overflow-hidden z-20 dark:bg-gray-800 border dark:border-gray-700">
                                    <div class="py-2">
                                        <div class="flex justify-between items-center px-4 py-2 border-b dark:border-gray-700">
                                            <span class="text-sm font-bold text-gray-700 dark:text-gray-200">Notificações</span>
                                            </div>

                                        <div class="max-h-80 overflow-y-auto">
                                            <div v-if="notifications.length > 0">
                                                <div v-for="notification in notifications" :key="notification.id" class="border-b dark:border-gray-700/50 last:border-b-0">
                                                    <Link :href="notification.data.url" @click="markAsRead(notification)" class="block px-4 py-3 text-sm text-gray-800 hover:bg-gray-100 dark:text-gray-300 dark:hover:bg-gray-700">
                                                        <p class="font-semibold">{{ notification.data.titulo }}</p>
                                                        <p class="text-xs">{{ notification.data.mensagem }}</p>
                                                    </Link>
                                                </div>
                                            </div>
                                            <div v-else class="px-4 py-8 text-center text-sm text-gray-500 dark:text-gray-400">
                                                Nenhuma nova notificação.
                                            </div>
                                        </div>
                                    </div>
                                </div>
                            </div>
                             <ThemeToggle />

                            <div class="relative user-menu">
                                <Dropdown align="right" width="48">
                                    <template #trigger>
                                            <button class="flex items-center rounded-full transition focus:outline-none focus:ring-2 focus:ring-[var(--color-primary)] focus:ring-offset-2 dark:focus:ring-offset-gray-900">
                                                <img class="h-9 w-9 rounded-full object-cover" 
                                                     :src="$page.props.auth.user.profile_photo_url" 
                                                     :alt="$page.props.auth.user.name">
                                            </button>
                                    </template>

                                    <template #content>
                                        <div class="block px-4 py-2 text-xs text-gray-400">
                                            Gerenciar Conta
                                        </div>
                                        <DropdownLink :href="route('profile.show')"> Perfil </DropdownLink>

                                        <DropdownLink v-if="isAdminTenant" as="button" @click="restartOnboardingTour">
                                            Reiniciar Tour
                                        </DropdownLink>

                                        <div class="border-t border-gray-200 dark:border-gray-600" />
                                        <form @submit.prevent="logout">
                                            <DropdownLink as="button">Sair</DropdownLink>
                                        </form>
                                    </template>
                                </Dropdown>
                            </div>
                        </div>
                    </header>

                    <main class="flex-1 overflow-y-auto p-4 sm:p-6 lg:p-8 scrollbar-thin scrollbar-thumb-[var(--color-primary)] scrollbar-track-slate-200 hover:scrollbar-thumb-slate-500 dark:scrollbar-track-slate-800 dark:hover:scrollbar-thumb-slate-400">
                        <slot />
                    </main>

                </div>
            </div>
        </div>
        <OnboardingTour :user-permissions="userPermissions" />
    </div>
</template>

